// Reverse lookups: from localized slug back to canonical (English) slug
export function getCanonicalJobSlug(lang: string, localizedSlug: string): string | undefined {
  const map = jobSlugsByLang[lang];
  if (!map) return undefined;
  return Object.entries(map).find(([, v]) => v === localizedSlug)?.[0];
}

export function getCountryCodeFromSlug(lang: string, localizedSlug: string): string | undefined {
  const map = countrySlugsByLang[lang];
  if (!map) return undefined;
  return Object.entries(map).find(([, v]) => v === localizedSlug)?.[0];
}


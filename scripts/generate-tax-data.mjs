#!/usr/bin/env node
// ============================================
// EuroSalary.eu — Tax Brackets Generator
// Reads data/tax-brackets/tax-brackets-2026.json
// Writes src/data/generated/tax-data.ts
//
// LAW 1: data → generator → code
// ============================================

import { readFileSync, writeFileSync, mkdirSync, existsSync } from 'fs';
import { join } from 'path';

const ROOT = decodeURIComponent(new URL('..', import.meta.url).pathname).replace(/^\/([A-Z]:)/, '$1');
const DATA_DIR = join(ROOT, 'data', 'tax-brackets');
const GENERATED_DIR = join(ROOT, 'src', 'data', 'generated');

if (!existsSync(GENERATED_DIR)) mkdirSync(GENERATED_DIR, { recursive: true });

const EXPECTED_COUNTRIES = 27;

function generate() {
  const sourceFile = join(DATA_DIR, 'tax-brackets-2026.json');
  if (!existsSync(sourceFile)) {
    console.error('  [generate-tax] ERROR: tax-brackets-2026.json not found');
    process.exit(1);
  }

  const raw = JSON.parse(readFileSync(sourceFile, 'utf-8'));
  const countries = raw.countries;
  const codes = Object.keys(countries);

  if (codes.length < EXPECTED_COUNTRIES) {
    console.error(`  [generate-tax] ERROR: Expected ${EXPECTED_COUNTRIES} countries, found ${codes.length}`);
    process.exit(1);
  }

  // Build TS entries
  const entries = codes.map(code => {
    const c = countries[code];
    const bracketsStr = c.brackets.map(b => {
      const high = b[1] === null ? 'Infinity' : b[1];
      return `[${b[0]}, ${high}, ${b[2]}]`;
    }).join(', ');

    return `  ${code}: { brackets: [${bracketsStr}], socialRate: ${c.socialRate}, childDeductionPerChild: ${c.childDeductionPerChild} }`;
  });

  const output = `// AUTO-GENERATED by scripts/generate-tax-data.mjs
// Source: data/tax-brackets/tax-brackets-2026.json
// Generated: ${new Date().toISOString()}
// DO NOT EDIT — re-run \`npm run generate:tax\` to update

export interface TaxCountryData {
  brackets: [number, number, number][];
  socialRate: number;
  childDeductionPerChild: number;
}

export const taxData: Record<string, TaxCountryData> = {
${entries.join(',\n')},
};

export const TAX_YEAR = ${raw.year};
export const TAX_LAST_UPDATED = '${raw.lastUpdated}';
export const TAX_COUNTRY_COUNT = ${codes.length};
`;

  const outFile = join(GENERATED_DIR, 'tax-data.ts');
  writeFileSync(outFile, output, 'utf-8');
  console.log(`  [generate-tax] ✓ ${codes.length} countries → ${outFile}`);
}

generate();
